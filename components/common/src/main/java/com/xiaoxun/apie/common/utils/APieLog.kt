package com.xiaoxun.apie.common.utils

import android.util.Log

/**
 * 日志工具类
 */
class APieLog {
    companion object {
        fun d(tag: String, msg: String) {
            Log.d(tag, msg)
        }

        fun e(tag: String, msg: String) {
            Log.d(tag, msg)
        }

        fun i(tag: String, msg: String) {
            Log.d(tag, msg)
        }

        fun v(tag: String, msg: String) {
            Log.d(tag, msg)
        }

        fun w(tag: String, msg: String) {
            Log.d(tag, msg)
        }
    }
}